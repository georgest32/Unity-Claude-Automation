# Debug-Simple-Return.ps1
# Simple test to isolate the return issue

Write-Host "Testing ConcurrentQueue return..."

function Test-Return {
    $obj = [System.Collections.Concurrent.ConcurrentQueue[object]]::new()
    return $obj
}

$result = Test-Return
Write-Host "Result: $result"
Write-Host "Is null: $($result -eq $null)"

# Test what type we get back
if ($result) {
    Write-Host "Type: $($result.GetType().FullName)"
} else {
    Write-Host "No result returned"
}
