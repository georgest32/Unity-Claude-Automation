# Wrapper script for MkDocs
# Automatically uses virtual environment version

$venvPath = Join-Path $PSScriptRoot ".venv\Scripts"
$mkdocsExe = Join-Path $venvPath "mkdocs.exe"

if (Test-Path $mkdocsExe) {
    & $mkdocsExe @args
} else {
    Write-Error "MkDocs not found in virtual environment. Run: pip install mkdocs mkdocs-material"
    exit 1
}
