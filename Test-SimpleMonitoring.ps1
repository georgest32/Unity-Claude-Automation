# Test-SimpleMonitoring.ps1
# Simplest possible monitoring script to test if it stays running

Write-Host "Starting simple monitoring test at $(Get-Date)" -ForegroundColor Green
Write-Host "PID: $PID" -ForegroundColor Yellow

$counter = 0
while ($true) {
    $counter++
    Write-Host "[$counter] Still running at $(Get-Date -Format 'HH:mm:ss')" -ForegroundColor Cyan
    Start-Sleep -Seconds 10
    
    if ($counter -ge 100) {
        Write-Host "Reached maximum count, exiting" -ForegroundColor Yellow
        break
    }
}
