# Start-SystemStatusMonitoring.ps1
# Wrapper script to start the System Status Monitoring module
# This version delegates to the working implementation
# Date: 2025-08-20


# PowerShell 7 Self-Elevation

param(
    [switch]$Debug = $false
)

if ($PSVersionTable.PSVersion.Major -lt 7) {
    $pwsh7 = "C:\Program Files\PowerShell\7\pwsh.exe"
    if (Test-Path $pwsh7) {
        Write-Host "Upgrading to PowerShell 7..." -ForegroundColor Yellow
        $arguments = @("-NoProfile", "-ExecutionPolicy", "Bypass", "-File", $MyInvocation.MyCommand.Path) + $args
        Start-Process -FilePath $pwsh7 -ArgumentList $arguments -NoNewWindow -Wait
        exit
    } else {
        Write-Warning "PowerShell 7 not found. Running in PowerShell $($PSVersionTable.PSVersion)"
    }
}

# Simply delegate to the working version that properly handles timer events
& "$PSScriptRoot\Start-SystemStatusMonitoring-Working.ps1" -Debug:$Debug
