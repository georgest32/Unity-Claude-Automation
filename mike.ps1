# Wrapper script for Mike (MkDocs versioning)
# Automatically uses virtual environment version

$venvPath = Join-Path $PSScriptRoot ".venv\Scripts"
$mikeExe = Join-Path $venvPath "mike.exe"

if (Test-Path $mikeExe) {
    & $mikeExe @args
} else {
    Write-Error "Mike not found in virtual environment. Run: pip install mike"
    exit 1
}
